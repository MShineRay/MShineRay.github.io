              遵循SRP不仅可以提升代码的可读性，还能提升代码的可复用性。因为职责越单一，功能越内聚，就越有可能被复用，这和代码的行数没有直接的关联性，但是有间接的关联性。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">3.7 精简辅助代码</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              所谓的辅助代码（Assistant
              Code），是程序运行中必不可少的代码，但又不是处理业务逻辑的核心代码，比如判空、打印日志、鉴权、降级和缓存检查等。这些代码往往会在多个函数中重复冗余，减少辅助代码可以让代码显得更加干净整洁，易于维护。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              利用Hystrix提供的API，我们可以使用注解的方式定义降级服务，从而不用在业务逻辑里面使用try/catch来做异常情况下的服务降级。一个典型的Hystrix的服务降级代码如下所示：public
              class UserService { @Autowired private RestTemplate restTemplate;
              @HystrixCommand(fallbackMethod = "defaultUser") public User getUserById(Long id){
              return restTemplate.getForObject("http://USER-SERVICE/users/{1}", User.class, id); }
              //在远程服务不可用时，使用降级方法：defaultUser public User defaultUser(){ return new
              User(); }}
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">3.9 SLAP</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              抽象层次一致性（Single Level of Abstration
              Principle，SLAP），是和组合函数密切相关的一个原则。组合函数要求将一个大函数拆成多个子函数的组合，而SLAP要求函数体中的内容必须在同一个抽象层次上。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">3.10 函数式编程</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              函数式编程中最重要的特征之一，就是你可以把函数（你的代码）作为参数传递给另一个函数。为什么这个功能很重要呢？主要有以下两个原因。·减少冗余代码，让代码更简洁、可读性更好。·函数是“无副作用”的，即没有对共享的可变数据操作，可以利用多核并行处理，而不用担心线程安全问题。
            </div>
          </div>
        </div>
      </div>
