              “分”就是递归地将原问题分解成小问题；“治”则是在解决了各个小问题（各个击破）之后合并小问题的解，从而得到整个问题的解。分治法解题的一般步骤如下。（1）分解：将要解决的问题划分成若干规模较小的同类问题。（2）求解：当子问题划分得足够小时，用较简单的方法解决。（3）合并：按原问题的要求，将子问题的解逐层合并，构成原问题的解。
            </div>
          </div>
        </div>
