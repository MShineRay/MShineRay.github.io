            <div class="text">
              什么是结构化思维呢？我给结构化思维的定义就是“逻辑+套路”。1．逻辑所谓逻辑，是指结构之间必须是有逻辑关系的。例如，你说话时用“第一、第二、第三”这个逻辑顺序是合理的，而如果用“第一、第二、第四”就会显得很奇怪。实际上，组织思想的逻辑只有4种。（1）演绎顺序：比如“大前提、小前提、结论”的演绎推理方式就是演绎顺序的。（2）时间（步骤）顺序：比如“第一、第二、第三”和“首先、再者、然后”等，大多数的时间顺序同时也是因果顺序。（3）空间（结构）顺序：比如“前端、后端、数据”和“波士顿、纽约、华盛顿”等，化整为零（将整体分解为部分）等都是空间顺序。在做空间分解时，要注意满足“相互独立，完全穷尽”（Mutually
              Exclusive Collectively
              Exhaustive，MECE）原则。（4）程度（重要性）顺序：比如“最重要、次重要、不重要”等。只要我们的思想和表达在这4种逻辑顺序之内，就是有逻辑的，否则就是没有逻辑的。2．套路套路是指我们解决问题的方法论、路径和经验。比如，5W2H分析法就是非常好的，是可以帮助我们分析问题的一个“套路”。试想一下，面对任何一个问题，你如果都能从“Why、Who、When、Where、What、How和How
