        <div class="sectionListItem_title">第4章 设计原则</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              每个人都有义务捍卫、遵守或完善原则。原则可以修正，但是不能肆意妄为。——瑞·达利欧《原则》
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.1 SOLID概览</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              SOLID是5个设计原则开头字母的缩写，其本身就有“稳定的”的意思，寓意是“遵从SOLID原则可以建立稳定、灵活、健壮的系统”。5个原则分别如下。·Single
              Responsibility Principle（SRP）：单一职责原则。·Open Close
              Principle（OCP）：开闭原则。·Liskov Substitution
              Principle（LSP）：里氏替换原则。·Interface Segregation
              Principle（ISP）：接口隔离原则。·Dependency Inversion Principle（DIP）：依赖倒置原则。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.3 OCP</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div><div class="text">OCP软件实体应该对扩展开放，对修改关闭。</div></div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              在面向对象设计中，我们通常通过继承和多态来实现OCP，即封装不变部分。对于需要变化的部分，通过接口继承实现的方式来实现“开放”。因此，区别面向过程语言和面向对象语言最重要的标志就是看它是否支持多态。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              装饰者模式，可以在不改变被装饰对象的情况下，通过包装（Wrap）一个新类来扩展功能；策略模式，通过制定一个策略接口，让不同的策略实现成为可能；适配器模式，在不改变原有类的基础上，让其适配（Adapt）新的功能；观察者模式，可以灵活地添加或删除观察者（Listener）来扩展系统的功能。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              当然，要想做到绝对地“不修改”是比较理想主义的。因为业务是不确定的，没有谁可以预测到所有的扩展点，因此这里需要一定的权衡，如果提前做过多的“大设计”，可能会犯YAGNI（You
              Ain’t Gonna Need It）的错误。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.5 ISP</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              ISP多个特定客户端接口要好于一个宽泛用途的接口。接口隔离原则认为不能强迫用户去依赖那些他们不使用的接口。换句话说，使用多个专门的接口比使用单一的总接口要好。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.6 DIP</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              模块之间交互应该依赖抽象，而非实现。DIP要求高层模块不应该依赖于低层模块，二者都应该依赖于抽象。抽象不应该依赖细节，细节应该依赖抽象。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              依赖倒置，就是要反转依赖的方向，让原来紧耦合的依赖关系得以解耦，这样依赖方和被依赖方都有更高的灵活度。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.7 DRY</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              DRY是Don’t Repeat
              Yourself的缩写，DRY原则特指在程序设计和计算中避免重复代码，因为这样会降低代码的灵活性和简洁性，并且可能导致代码之间的矛盾。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              系统的每一个功能都应该有唯一的实现。也就是说，如果多次遇到同样的问题，就应该抽象出一个共同的解决方法，不要重复开发同样的功能。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.8 YAGNI</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div><div class="text">YAGNI（You Ain’t Gonna Need It）的意思是“你不会需要它”</div></div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              YAGNI是针对“大设计”（Big
              Design）提出来的，是“极限编程”提倡的原则，是指你自以为有用的功能，实际上都是用不到的。因此，除了核心的功能之外，其他的功能一概不要提前设计，这样可以大大加快开发进程。它背后的指导思想就是尽可能快、尽可能简单地让软件运行起来。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              你会发现DRY原则和YAGNI原则是不兼容的。前者追求“抽象化”，要求找到通用的解决方法；后者追求“快和省”，意味着不要把精力放在抽象化上面，因为很可能“你不会需要它”。因此，就有了Rule
              of Three原则。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.9 Rule of Three</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              Rule of Three也被称为“三次原则”，是指当某个功能第三次出现时，就有必要进行“抽象化”了。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              三次原则指导我们可以通过以下步骤来写代码。（1）第一次用到某个功能时，写一个特定的解决方法。（2）第二次又用到的时候，复制上一次的代码。（3）第三次出现的时候，才着手“抽象化”，写出通用的解决方法。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_divider leftIndent"></div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div>
            <div class="text">
              软件设计本身就是一个平衡的艺术，我们既反对过度设计（Over
              Design），也绝对不赞成无设计（No Design）。
            </div>
          </div>
        </div>
      </div>
      <div class="sectionListItem">
        <div class="sectionListItem_title">4.10 KISS原则</div>
        <div class="sectionListItem_content noteItem_content clickable">
          <span class="icon"></span>
          <div><div class="text">KISS（Keep It Simple and Stupid）</div></div>
        </div>
