              摧龙八式摧龙八式是以客户采购流程、行为和心理为核心的销售方法论，并提供理论模型和管理工具。作为指导销售的思路，它必须配合与采购阶段相对应的一系列销售技巧，才能推进销售的发展。摧龙八式可以帮助企业建立统一的销售方法论，形成固定的销售套路和章法，为客户创造价值并推动和引导采购流程发展，缩短销售周期，最终提高销售的效率和赢率，降低销售成本。[插图]摧龙八式还可以形成一套企业内部的管理销售的语言，使销售过程便于理解，加强内部沟通。摧龙八式还是销售漏斗管理的理论基础，公司管理层通过销售漏斗，可以做到“吃着碗里的订单，看着盆里的销售线索，惦记着锅里的目标客户”，不断优化销售流程。1
