              设计在采购的前两个阶段，销售的核心是围绕客户需求。然而需求不难满足，竞争对手才是导致销售困难的关键。[插图]在设计阶段，客户把需求转变为明确和量化的采购指标，用于潜在供应商的评估，这是难得的屏蔽对手的时机。在正规的招投标中，采购指标将进入招投标文件，其打分结果将直接决定销售结果。如果成功地引导客户采购指标，就能将竞争对手引入对我们有利的战场。我们应该介入客户采购指标的设计，帮助客户建立正确和完善的采购标准，为客户创造价值。5
