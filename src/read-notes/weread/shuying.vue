              立项在采购过程中，决策者也许不参与采购，仅仅做出五个决定：是否采购、采购时间、预算、最终厂家、价格和条款。在立项阶段，决策者将做出其前三个决定。[插图]决策者往往经验丰富，见多识广，直截了当，时间有限，并且往往不会听信厂家的一面之词，更愿意根据下属的汇报做出判断。他们更关心战略发展、业务流程、组织和人员发展、客户满意度以及市场竞争状态等宏观层面，而非产品和服务的细节。决定预算的依据是采购的投资回报率，我们必须在很短时间内，将明确的投资回报或者利弊分析呈现出来，促使他们做出决定。4
