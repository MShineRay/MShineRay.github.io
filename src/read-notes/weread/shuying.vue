              赢取承诺采购流程进入购买承诺阶段，成交就是一手交钱一手交货。然而客户将会重新回顾整个购买过程，如果意识到风险，便会暂停采购。如果竞争对手打消了客户的顾虑，便有可能抢去生意，我们便替他人做嫁衣了。因此，必须缓解客户的顾虑，才能推动客户做出购买决定。风险是导致客户产生顾虑的原因，例如买房的顾客可能担心，房价会不会下降，贷款能不能办好，房产证能不能按时拿到，房子买到后能不能租出去。要想缓解客户顾虑，必须先找出客户意识到的风险，并采取对策。[插图]客户有顾虑的标志往往包括，例如，“稍微等等……”“我们还在研究……”“最近比较忙……”或者引荐他人，例如领导，相关主管和专家等等。在成功缓解客户顾虑之后，客户通常要讨价还价，这通常是谈判的过程。7
