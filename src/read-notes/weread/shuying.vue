              1万元，答应年底还，如果他打算还其中一人，很可能先还B，以维持信用，这说明催款要及时。此外，催款的竞争程度甚至超过销售，范围也更广，税务、水电煤气房租、员工、其他供应商都在竞争获取客户的账款。欢迎下载赢销动力，形成您的企业的销售套路和共同销售语言。
