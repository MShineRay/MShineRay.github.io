              建立信任信任、需求、竞争、风险、期望值是销售的核心要素，其中信任是销售的基石，必须首先满足，如果不能建立信任，便难以深入挖掘需求，销售便是纸上谈兵，难以深入。因此，建立信任是摧龙八式的第一个步骤。[插图]在建立信任的过程中，我们常常遇到以下问题：第一，难以界定关系阶段，将销售建立在不牢固的基础上；第二，采用“三板斧”的销售方法，吃喝玩乐，不能针对每个客户的独特兴趣点，不能找到推进关系的方法；第三，在推进关系的过程中，投入很大精力和销售费用，却需要很长的时间；第四，信任关系只能覆盖中低层级客户，难以覆盖决策层客户。2
              /
              发掘需求发现需求是采购的第一个阶段，客户常常只能发现部分需求。当我们建立信任关系之后，便可以帮助客户全面、完整、清晰地发现需求，为客户创造价值。[插图]客户需求是销售的核心，很多人在没有充分理解客户需求的情况下推荐产品和方案，导致销售失败。销售人员在发掘需求时，经常会犯三个错误：第一是不全面，例如只询问手机屏幕尺寸，忽略了品牌、价位、功能、制式等其他方面的需求；第二是不清晰，只了解到客户要求大屏幕手机，而没有询问到底多大才算大；第三是不深入，不了解客户是给父亲买手机，所以才希望屏幕大。要想提高发掘需求的能力，必须学会倾听和提问。对于倾听，必须学会用心倾听，用目光观察，同时必须掌握开放式、排除式、因果式、提示式以及总结式提问方式，并养成习惯。这些都是最核心的销售技巧。3
